/**
 * Comprehensive Zone Data for Lead Discovery
 *
 * Each zone has:
 * - Precise coordinates (lat/lng)
 * - Zone type (COMMERCIAL, INDUSTRIAL, TOURIST, MEDICAL, MIXED)
 * - Business types that thrive in this zone
 * - Priority score (1-10) - higher = search first
 * - Search radius in km
 */

export type ZoneType =
  | "COMMERCIAL"
  | "INDUSTRIAL"
  | "TOURIST"
  | "MEDICAL"
  | "EDUCATION"
  | "MIXED";

export interface Zone {
  name: string;
  lat: number;
  lng: number;
  radiusKm: number;
  type: ZoneType;
  businessTypes: string[];
  priority: number; // 1-10, higher = search first
  description?: string;
}

export interface CityData {
  name: string;
  state: string;
  zones: Zone[];
}

