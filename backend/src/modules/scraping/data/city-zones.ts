/**
 * Get zones for a city filtered by business type
 */
export function getZonesForBusinessType(
  city: string,
  businessType: string,
): Zone[] {
  const cityData = CITIES.find(
    (c) => c.name.toLowerCase() === city.toLowerCase(),
  );
  if (!cityData) return [];

  return cityData.zones
    .filter((zone) => zone.businessTypes.includes(businessType))
    .sort((a, b) => b.priority - a.priority);
}

/**
 * Get all zones for a city sorted by priority
 */
export function getAllZones(city: string): Zone[] {
  const cityData = CITIES.find(
    (c) => c.name.toLowerCase() === city.toLowerCase(),
  );
  if (!cityData) return [];

  return [...cityData.zones].sort((a, b) => b.priority - a.priority);
}

/**
 * Get zones by type
 */
export function getZonesByType(city: string, type: ZoneType): Zone[] {
  const cityData = CITIES.find(
    (c) => c.name.toLowerCase() === city.toLowerCase(),
  );
  if (!cityData) return [];

  return cityData.zones
    .filter((zone) => zone.type === type)
    .sort((a, b) => b.priority - a.priority);
}

/**
 * Check if city is supported
 */
export function isCitySupported(city: string): boolean {
  return CITIES.some((c) => c.name.toLowerCase() === city.toLowerCase());
}

/**
 * Get list of supported cities
 */
export function getSupportedCities(): string[] {
  return CITIES.map((c) => c.name);
}
